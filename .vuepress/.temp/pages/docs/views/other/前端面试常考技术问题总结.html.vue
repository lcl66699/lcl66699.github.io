<p>8）vue生命周期的理解</p>
<blockquote>
<p>vue实例有一个完整的生命周期，生命周期也就是指一个实例从开始创建到销毁的这个过程</p>
</blockquote>
<ul>
<li><code v-pre>beforeCreate()</code> 在实例创建之间执行，数据未加载状态</li>
<li><code v-pre>created()</code> 在实例创建、数据加载后，能初始化数据，<code v-pre>dom</code>渲染之前执行</li>
<li><code v-pre>beforeMount()</code> 虚拟<code v-pre>dom</code>已创建完成，在数据渲染前最后一次更改数据</li>
<li><code v-pre>mounted()</code> 页面、数据渲染完成，真实<code v-pre>dom</code>挂载完成</li>
<li><code v-pre>beforeUpadate()</code> 重新渲染之前触发</li>
<li><code v-pre>updated()</code> 数据已经更改完成，<code v-pre>dom</code> 也重新 <code v-pre>render</code> 完成,更改数据会陷入死循环</li>
<li><code v-pre>beforeDestory()</code> 和 <code v-pre>destoryed()</code> 前者是销毁前执行（实例仍然完全可用），后者则是销毁后执行</li>
</ul>
<p>注意：在整个周期中第一次能拿到data数据(data数据的初始化)的是在created</p>
<p>https://blog.csdn.net/weixin_42460870/article/details/80696763</p>
<p><code v-pre>beforecreated</code>：el 和 data 并未初始化</p>
<p><code v-pre>created</code> :完成了 data 数据的初始化，el没有
<code v-pre>beforeMount</code> ：完成了 el 和 data 初始化
<code v-pre>mounted</code> ：完成挂载</p>
